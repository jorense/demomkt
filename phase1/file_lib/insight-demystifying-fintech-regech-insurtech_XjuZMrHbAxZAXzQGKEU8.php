							<div class="in-par pt5 anim-content animUp">


								<p><strong>Balancing customer experience with risk management </strong>

								<p>Financial institutions, whether domestic or international, are feeling the competitive pressure to increase revenues, lower operating costs, and delight customers. As such, business models are being redesigned and the use of innovative solutions is getting higher attention to address customer pain points. Although only 37% of all organizations have seriously adopted digitalization in 2016 according to the survey by the World Bank Group, many banks - both retail and non-retail sectors - have commenced their transformation journeys to provide anytime, anywhere banking services, ensuring their processes are aligned to customer expectations and their products and services are insight-based to provide added value. </p>

								<p>FinTech players have been introducing technologies such as biometrics to help speed up Know-Your-Customer (KYC) checks to verify customer identity during on-boarding, blockchain to validate required information that are across multiple entities, and AI-powered chatbots that can takeover agents to handle routinary and simple queries instead of human interventions. </p>


								<p>While financial institutions need to make sure their customers’ information is protected in the era of high cybercrime, they don’t have to compromise service. With the right strategy and technology partner, a growing number of banks are finding a balance where security and customer experience intersect. </p>

								<p>Simplified sign-on makes it easier for customers to use services if multifactor authentication and integrating single sign-on (SSO) are provided. This convenient process lessens customer frustration and speeds up their ability to perform banking tasks. Also, it is vital to keep sensitive company information secured and create an ongoing fraud awareness program for both employees and customers. Engaging a technology provider with proven data-security expertise could prevent data breaches and other forms of cybercrime, thus minimizing not just financial loss but also maintaining customer trust and mitigating reputational risks. </p>

							</div>


							<div class="in-par pt5 anim-content animUp">


								<p><strong>Smart contract for insurance industry </strong>

								<p>Insurance organizations can consider using blockchain technology to streamline payments of premiums and claims. In addition, blockchain technologies can support significant digital transformation upcoming in the industry since much of the transformation relies on data. For example, actuaries and underwriters are using the ever expanding universe of data to build models to estimate risk and determine pricing more accurately. </p>

								<p>During the industry forum, the handling of customer claims was used to highlight typical bottlenecks or issues that customers and insurance organizations face and how blockchain could help. For customers, insurance contracts are typically complex and difficult to understand because of the legal language used. In addition, when accidents happen, customers can often be faced with a complex and drawn-out claims process. From the insurer’s perspective, the industry is facing ever tighter regulation and growing threats from fraud. </p>

								<p>Smart contracts driven by blockchain could provide customers and insurers with the means to manage claims in a transparent, responsive, and irrefutable manner. Contracts and claims could be recorded onto blockchain and validated by the network, ensuring only valid claims are paid. For example, the blockchain would reject multiple claims for one accident because the network would know that a claim had already been made. Smart contracts would also enforce the claims triggering payments automatically when certain conditions are validated. </p>

								<p>Adopting a common blockchain across the sector could create a step change in value for the insurance industry and claims handling could become more efficient and streamlined, resulting in an improved customer experience. Such an approach could also help to reduce further fraud if identity management was also enforced on the blockchain. </p>

								<p>The case implied that a common claims handling platform would still make it possible for individual insurers to compete for customers, offering a range of products and prices by virtue of the smart contracts they set up. Moreover, a blockchain would allow the industry as a whole to streamline its processing and offer a better user experience for customers who have to make a claim. Simultaneously, storing claims and customer information on blockchain would cut down fraudulent activities. </p>

