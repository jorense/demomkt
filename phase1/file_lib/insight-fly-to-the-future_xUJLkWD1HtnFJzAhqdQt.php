								<p><strong><span style="font-size: 25px;">I</span>f “Back To The Future” were a Hong Kong movie, there would likely be scenes where Marty from 1985 arrives in 2015 at the airport in Chek Lap Kok (no longer Kai Tak!) and finds it incredibly technological. However, such technological advances would not seem so unthinkable to us today. Indeed, with these digital solutions our travel experience can be largely enhanced.</strong></p>

								<p><strong>Runway to an Intelligent Airport</strong></p>

								<p>The Hong Kong International Airport handled 63.3 million passengers last year, representing an annual growth of 5.7% from 2013. Similarly, the increase in aircraft movement was about 5.1%, meaning the handling capacity of the airport is increasingly under pressure. To ease the ever-growing “workload”, apart from the newly completed West Apron Expansion which offers more parking stands for planes and other related supporting facilities, and the Midfield Development which includes a new 5-level concourse due to be completed by the end of this year, <a href="#">innovative digital solutions</a> provided by PCCW Solutions can be the answers.</p>

								<p>With digital technologies like real-time crowd monitoring, digitalised traveller information collection, travel pattern analysis, etc., the airport’s overall operation including queue time management, allocation of workforce, security, even “no show” passengers locating can be improved. Similarly, <a href="#">retail businesses will also benefit as with the introduction of technologies</a> like interactive digital shopping wall, iBeacon voucher, and the availability of Big Data for consumer behaviour analysis and marketing campaign planning, shopping will be made more convenient and pleasurable, hence higher sales revenue.</p>

