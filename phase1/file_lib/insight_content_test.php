								<p><strong><span style="font-size: 25px;">A</span>s the electric vehicle (EV) market burgeons globally, so to do the endless innovative solutions in creating ‘smart cities’ – and PCCW Solutions is at the forefront of this innovation in Hong Kong, with its award-winning ‘Infinitum Parking and Charging’ solution.</strong></p>
								<p>As the electric vehicle (EV) market burgeons globally, so to do the endless innovative solutions in creating ‘smart cities’ – and PCCW Solutions is at the forefront of this innovation in Hong Kong, with its award-winning ‘Infinitum Parking and Charging’ solution.</p>
								<p>PCCW Solutions’ Infinitum Parking and Charging solution is based on the world-class Infinitum™ IoT platform.</p>
								<p>Fundamentally, it is an innovation created to improve on-road experiences for modern EV drivers. The Infinitum Parking and Charging solution is incredibly user-friendly and easily integrates into existing automotive infrastructure, eliminating the need for operator’s to completely overhaul their current EV platforms.</p>
								<p>There are four main components to the Infinitum Parking and Charging solution: mobile app; smart parking; smart charging; and customer relationship management.</p>
								<p>The mobile app is the user’s side of the solution. It enables drivers to find and reserve parking and charging facilities, across all of Hong Kong, in real-time. The app can navigate your EV to your reserved spot for you, suggest alternate routes and manage payment.</p>
