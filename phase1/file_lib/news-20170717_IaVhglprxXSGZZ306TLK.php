								<p>The contract award ceremony was held at the Central Government Offices, Tamar on 17 July 2017.</p>

								<p>PCCW Solutions has been awarded in all four categories of services under the latest Standing Offer Agreement for Quality Professional Services (“SOA-QPS4”) to provide various professional IT services to HKSAR Government’s bureaux and departments effective from 31 July 2017 for 48 months. The SOA-QPS4 categories include (Category 1) pre-implementation &amp; independent program/ project management services; (Category 2) on-going services; (Category 3) implementation &amp; combined system development services; and (Category 4) information security and independent testing services.</p>

								<p>The SOA-QPS scheme is part of HKSAR Government's IT outsourcing strategy to enlarge its delivery capacity for IT services and accelerate the delivery of IT solutions to support the increasing demand of quality e-Government services.</p>

								<p>PCCW Solutions has been a long-term contractor under the SOA-QPS scheme since 2005. With a proven track record of delivering premium IT services and strong competence in upholding quality excellence, PCCW Solutions is committed to be the valued partner to support the Government in provision of better public services.</p>

