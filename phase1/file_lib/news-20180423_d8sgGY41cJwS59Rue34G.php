								<p>PCCW Solutions was named the winner of Silver Stevie<sup>®</sup> Award in the Award for Innovation in Content Marketing and Bronze Stevie<sup>®</sup> Award in the Award for Innovation in Social Media Marketing in the fifth annual Asia-Pacific Stevie Awards.</p>
 
 
				<p>The Asia-Pacific Stevie Awards are the only business awards program to recognize innovation in the workplace in all 22 nations of the Asia-Pacific region. The Stevie Awards are widely considered to be the world's premier business awards, conferring recognition for achievement in programs such as The International Business Awards<sup>®</sup> for sixteen years.</p>
					 
				<p>Nicknamed the Stevies for the Greek word for “crowned,” the awards will be presented to winners at a gala banquet at the Mira Hotel in Hong Kong on Friday, 1 June. </p> 

<p>More than 800 nominations from organizations across the Asia-Pacific region were considered this year in categories such as Award for Excellence in Innovation in Products &amp; Services, Award for Innovative Management, and Award for Innovation in Corporate Websites, among many others. PCCW Solutions won in the Award for Innovation in Marketing category.</p>

<p>Mr. Ramez Younan, Managing Director of PCCW Solutions, said, “Winning these acclaimed awards proves that PCCW Solutions is always growing and improving as a firm, is known for enabling digital transformation within the marketplace, and is a testament to our best-in-class marketing efforts.”</p>

<p>Gold, Silver and Bronze Stevie Award winners were determined by the average scores of more than 100 executives around the world acting as judges in March and April.</p>

<p>“We are very pleased that entries to this program grew by more than 30% this year,” said Michael Gallagher, president and founder of the Stevie Awards. “This growth illustrates how vital innovation is to business growth, and how important being recognized for their innovation is to enterprises throughout the Asia-Pacific region. We extend our warmest congratulations to all of this year’s Gold, Silver and Bronze Stevie Award winners.”</p>

<p>Details about the Asia-Pacific Stevie Awards and the list of Stevie Award winners are available at <a href="http://Asia.Stevieawards.com." style="color: #337ab7;" target="_blank">http://Asia.Stevieawards.com</a>. </p>
				
