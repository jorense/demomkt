								<p><strong>Technology Transition to Artificial Intelligence</strong></p>

								<p>In the age of emerging technology and increasing demand for digitalization, Artificial Intelligence has become a driving force of the technology evolution. Big enterprises are now transitioning their products and services through AI to become more sophisticated and pervasive, in order to create better interactions for their customers. Let’s take Walmart for example, which uses shelf scanning robots to check for all factors including pricing, misplaced items and stock levels within the store. This would help to redirect employee efforts to other, more critical tasks in the stores. </p>

								<p>Enterprises are also increasingly adopting the use of AI in various areas of their business, including security. Other organizations are utilizing AI through video platforms to make security surveillance more effective - capable of fully automating operations and predicting human behavior through analytics. Intelligent video analytics monitor video streams in near real time, identifying attributes, events or patterns of monitored environments.</p>

								<p>As applications continue to develop, predictive video analytics could facilitate incident preventions ranging from traffic issues, to terrorist threats. </p>

