function removeSpaceAndSpecialCharacters($val = "")
{
	$output = str_replace(" ", "_", $val);
	$output = str_replace(",", "", $output);
	$output = str_replace("&", "", $output);
	$output = str_replace("amp;", "", $output);
	$output = str_replace("<br>", "", $output);
	$output = str_replace("(", "", $output);
	$output = str_replace(")", "", $output);

	return $output;
}
