							<div class="pb1 anim-content animUp">
								<p><strong><span style="font-size: 25px;">O</span>utsourcing as a business practice has existed for a few decades now, with multiple objectives including cost, to let the focus the Enterprises focus on core business and let the non-core be done by another organisation et al, eventually helping the buyer organisation to be more efficient. From the perspective of suppliers, volume &amp; variety of services help them get better with time and eventually passing on more benefits to the buyer. Delivering more and more value with time, at lesser cost and lesser time is non-negotiable. If anyone in the Supply Chain of services, for any Industry does not focus on this, survival will become difficult. </strong></p>

								<p>Innovation is not a choice – It is necessity for survival and it cannot be done in isolation. It has to take into consideration different stakeholders in the Eco System and the evolution of different parts around it. The whole concept of Buyer-Supplier needs to be relooked for better benefit and enhanced outcome. </p>

								<p>If we look at Outsourcing of IT Services, it has gone through a change with time and it continues to evolve. Lets look at them in the form of 3 Waves. Interestingly, emergence of the next wave has not meant that the earlier Wave became irrelevant. It has continued to Co-exist to give more options for everyone involved.
</p>



							</div>



							<div class="in-par pt5 anim-content animUp">



								<div class="so-full-pic-holder inview" style="transition-delay: 0.1s;">
