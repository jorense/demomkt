								<p>There will continue to be a need for a trusted partner, serving as a client advocate thinking about the buyer, and its drivers while executing what is appropriate for the business. This would mean changing the way SIs work, as they would need to gear up to take on the role of ‘Trusted Advisors &amp; Solution Orchestrators’. While being an orchestrator, the SI will need to pick the appropriate solution components from the larger eco system, stitch them appropriately and continue to dynamically make changes as the business progresses. This orchestration is only going to become more complex with more solution providers and new set of problems requiring immediate attention. Even the best solution from a startup would need to work in the context of the client organization with the existing landscape and newer areas which keep getting added with time. </p>

								<p>Speed is of utmost importance while all of this is being done. This would mean becoming agile, changing fast, learning faster and making the eco-system of technology providers and consumers, smarter. </p>

								<p>The game will be more challenging so system integrators will need to change dynamically to provide substantially more value and higher quality at a lesser cost, by leveraging new technological components, start-ups, cloud based solutions and boutique companies resolving niche problems. They will need to think more from a business perspective and eventual impact of the solution, rather than being a technology solution provider, in isolation.</p>

								<p>In these exciting times, when technology becomes the reason for fourth Industrial revolution, only the paranoids who out-smart the rest, will survive. </p>

