								<p><strong>Humanizing Video Service</strong></p>
								<p>Taking out human actions in video surveillances results in increased operational efficiency and reductions in chances of human error. The advance machine learning algorithms in video feeds automatically enables cameras to recognize people, objects, and situations. This benefit not just predicting potential threats in security and safety of people, but also enables curation of people and their behavior to data as valuable analytics.</p>

								<p>With the emergence of AI, deep learning has enabled video analytics to analyse facial data faster and more accurately. Facial recognition is just a step in video analytics, through AI, it provides a complete suite of capabilities, which includes, people management, vehicle management, behavior monitoring, and device protection<sup>1</sup>.</p>

								<p>In the retail industry, video analytics provide advanced information like heat mapping to analyze foothold and customer traffic, as well as age estimation and gender detection for targeted communication. The resulting mined data on customer behaviour can then be used to plan marketing strategies<sup>2</sup>.</p>

							</div>

							
							<div class="in-par pt5 anim-content animUp">
								
								<p><strong>Intelligent Video Analytics in Airports </strong></p>

								<p>Being one of the busiest places in most cities, airports stand to benefit with the use of intelligent video analytics. By incorporating AI in video surveillance, the system’s analytics can easily detect abnormal behaviour and suspicious individuals.</p> 

								<p>Considering that airports today have retail shops to enhance customer experience, these retail businesses can harness important information to strategically place their digital signage and promotions.</p>

								<p>Intelligent video analytics will also provide helpful insight for airport management to use the data gathered as reference for rent calculation, design layout, and placement of facilities and shops to further enhance customer experience and maximize profit<sup>3</sup>.</p>

								<p>With AI being the clear future of digital technology, enterprises and individuals need to gear up to be ready to adopt and use AI considering the possibilities of AI creating not just better businesses, but also a better world. </p>
																

