								<p><strong>PCCW (SEHK: 0008) – HONG KONG, May 31, 2018</strong> – <span style="white-space: nowrap;">PCCW Solutions</span>, the IT services flagship of PCCW Limited, today unveils Infinitum™ Visum, an Internet connected mirror with an interactive high-definition touch screen combined with a pre-loaded suite of productivity, convenience and entertainment applications. Infinitum™ Visum empowers connected digital lifestyles and helps transform the way people live, work and play.

				<p>Customized Infinitum™ Visum versions will ship with pre-defined packages of industry targeted applications such as for property developers, hospitality providers and in-store retail use. The multi-functional Visum system enables property developers to bring home-automation innovations to residents, empowers hoteliers to revolutionize guest experiences and enables retailers to provide in-store virtual try-on and seamless online-to-offline shopping experiences to the shoppers.</p>
				 
				<p>With built-in Wi-Fi and Bluetooth connectivity, Infinitum™ Visum supports connectivity to IoT smart devices such as fitness trackers, digital scales, blood pressure monitors and a wide variety of medical devices to bring external information into an easily accessed and elegant home appliance. Infinitum™ Visum also supports entertainment applications such as OTT video service and music streaming to enrich the daily life of end-users. <span style="white-space: nowrap;">PCCW Solutions</span> tailors packaged solutions for enterprises with software, hardware configuration and user experience optimization to provide back-end data analytics, reporting and other services to address their specific needs. </p>


				<p>Mr. Ramez Younan, Managing Director of PCCW Solutions, said, &quot;With combined capabilities of connectivity, entertainment and information technology from PCCW Group, Infinitum™ Visum is a unique and intelligent solution that can be configured to enhance digitally connected lives in an ever expanding digital world. <span style="white-space: nowrap;">PCCW Solutions</span> is committed to bringing innovative solutions such as Visum and other Infinitum™ suite of solutions to support business across many industries to transform digitally.&quot; </p> 

				<p>&quot;Visum&quot; means &quot;view&quot; in Latin, while &quot;Infinitum™&quot; is the solutions suite from <span style="white-space: nowrap;">PCCW Solutions</span>. Infinitum™ suite comprises of five pillars, namely Cloud, AI, Digital, Advanced Analytics and IoT solutions. </p> 
 



