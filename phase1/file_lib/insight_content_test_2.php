							<div class="pb1 anim-content animUp">
								<p><strong><span style="font-size: 25px;">T</span>ransitioning to AR/VR</strong></p>
								
								<p>Virtual reality (VR) and augmented reality (AR) are gaining ground in the enterprise. The business of using AR/VR has been a growing trend and indeed a promising lucrative business proposition for companies to engage into. These technologies have made companies to reconnect with their customers in a more engaging and interactive way. Different industries are now experimenting and applying the technology and starting to change the way brands are marketed to its customers. </p>

								<p>While not as widespread as virtual reality, augmented reality is more dominate and continues to make leaps. Experts predicted that VR and AR will be a US $150 billion market by 2020, with $120 billion of that dedicated to AR. Based on the recent survey, 67% of enterprises are considering adopting AR while VR is said to be 48%. Thanks to AR technology, we are entering an era where marketing can hybridize physical and digital experience. </p>

							</div>
							
							
							<div class="in-par pt5 anim-content animUp">
								<p><strong>New Age of Customer Engagement</strong></p>
								<p>Executing marketing campaigns using AR/VR technology is all about storytelling, but done in an immersive, and close to experience kind of interaction between customers and the brand. Gone are the traditional collaterals marketers would provide customers to give information about their company offerings. Marketers now let their customers experience their brand first-hand, providing better interactions and revolutionizing the customer journey to drive company revenue. </p>

								<p>This is the reason why creative content is crucial for marketers to execute through the use of the technology. Based on a study, 70% of marketing executives are going to spend more on marketing tech this 2017 and will continue to grow . All of these are driven by the innovations of the digital society, dominated by mobile devices majority of people use, especially when they do online purchases. Marketers can maximize their brand’s online presence through the use of AR to create new markets and revenue growth.</p>
								
							</div>

							<div class="in-par pt5 anim-content animUp">
								<p><strong>Customer Journey Mapping</strong></p>
								<p>A customer journey is a set of interactions that a customer has with your business from awareness to purchase. It enables companies to experience first-hand how they can build a better brand, capture their customers effectively and uncover innovation opportunities. At PCCW Solutions, we advocate four stages of customer journey: Aware, Explore, Experience, and Retain. Here are some examples of how to take advantage of AR/VR to create better brand and maximize customer satisfaction along the journey: </p>


								<div class="so-full-pic-holder">
									<img class="img-auto" src="dist/images/insight/insight_p041.jpg" width="1471" height="1469" alt="">
								</div>


								<p><strong>Aware</strong> – Every customer journey is initiated by ensuring customer is becoming aware of your brand and is starting to consider you as a possible option. As the marketer, your question then becomes, “How do I connect with this potential customer?” Alibaba launched the Buy+ virtual shopping campaign, allowing shoppers in China to browse and buy all over the world. The program reached customers through a variety of digital channels, redefining branding and marketing in today’s internet era. For retailers who do not have a brick-and-mortar presence in their target markets, this could be a game changer. </p>

								<p><strong>Explore</strong> – Developing creative ways to expose target customers in exploring the brand is the most effective way businesses differentiate themselves from competitors. Providing location-based solutions to deliver compelling marketing content, engaging 3D content, as well as platforms that develops effective e-commerce and real time live support are just a few of the solutions companies can use to communicate with their customers. Major motion pictures like Star Wars, and Jurassic World used virtual content to generate interest, excitement, and strong memory associated with the brand.</p>

								<p><strong>Experience</strong> – Experiencing the brand at its most personal state is what truly wins customers, and that is what companies aim to do in all their campaigns. Engaging content through Internet of Things, personalized communication through social connections, to the best technology offered by AR/VR are just some of the leading companies can utilize to create better brand story. Adidas used this approach by utilizing AR/VR in their stores, to let customers fit clothes in a simulated fitting room on a virtual environment. The main goal is to let customers experience the benefits of the product first-hand and appreciate the brand the best way possible, and have fun while doing it.</p>

								<p><strong>Retain</strong> – Attracting new customers while retaining and growing the relationships with existing customers are two of marketers’ top priorities especially for the fast moving industries. <a href="#">Pizza Hut Hong Kong</a> partnered with PCCW Solutions in coming up with an interactive game using augmented reality technology to enhance customer engagement. We designed the AR game with high-quality 3D animated graphics and great user experience created a huge buzz to Pizza Hut’s customers and increased the stickiness to its brand. The AR/VR technology has created an effective product visualization for customers to experience and proves to be a huge deal, especially for brands in the retail industry.</p>
								
							</div>

							<div class="in-par pt5 anim-content animUp">
								<p><strong>The Next Move </strong></p>
								<p>AR/VR technology is going to be an important technology this 2017 and beyond. Companies should start to look at how to realize its potential while the technology continues to evolve. Marketers need to plan and strategize in integrating AR/VR into their marketing campaigns so that it enhances customer experience – make it easier, more fun and more convenient and builds better brand to drive revenue growth. Compelling content through AR/VR is crucial in order to deliver an effective immersive interaction between the customer and the brand. The key to adopting AR/VR is defining the specific activities where it can create real value for the customer. </p>
								
