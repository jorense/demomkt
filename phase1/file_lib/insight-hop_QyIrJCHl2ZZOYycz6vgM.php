								<p><strong><span style="font-size: 25px;">W</span>ith great variety and large number of e-retailers and logistics service providers, it sets no geographic boundary for us by making use of digital devices, and the purchased items from around the globe can be delivered at our place within just a few days. </strong></p>

								<p>While shopping online is effortless at your fingertips, the traditional and one-way parcel delivery may not be that fun. E-shoppers may have to wait for the parcels at home for a period of time and sometimes they may even miss the delivery time. On the other hand, e-retailers only provide limited delivery options and most of them cannot keep up with this <strong>digital era in logistics fulfillment areas</strong>. </p>

								<p>With an intention to empowering e-retailers and logistics service providers to provide a <strong>seamless customer experience</strong> and run <strong>Online-to-Offline business</strong>, PCCW Solutions and LTF Asia Limited, an Asia-based logistics solutions provider, jointly launch the digital logistics platform - HOP! (House of Parcels). With HOP!, e-shoppers can track their parcels online and they are given autonomy to pick up their parcels at their own pace within 48 hours from more than 40 HOP! machines all over Hong Kong. PCCW Solutions takes care of each touch point of customer journey, enabling e-shoppers to enjoy <strong>comprehensive and flexible delivery services</strong> from self-pick-up to home delivery.
</p>

								<p>To help e-retailers achieve more fruitful results, HOP! machine itself acts as an advertising medium, equipped with LED monitors and tailor-made stickers for promoting e-retailers’ goods. HOP!, without doubt, is not only generating more values to e-commerce players, but also increasing more footfall to location partners who provide high-traffic locations to HOP!. </p>

