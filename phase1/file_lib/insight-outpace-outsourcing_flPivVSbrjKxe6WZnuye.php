								<p class="pt5"><strong>Wave 1: Sourcing the Skill for a period, for a rate</strong></p>

								<p >The tradition Outsourcing focused on providing skilled people to buyer organisation for a particular period and billed as per the time they would spend doing work for the clients. These people should have the required skills and ability to execute the job as per client’s mandate. The clients dictate what they need and take control of the outcome. </p>

								<p><strong>Wave 2: Sourcing fixed outcome for a fixed price</strong></p>

								<p >This is a more mature model and risk is shared by both parties. In this case, scope is key and it is important to ensure that both sides are sufficiently protected for grey areas to avoid issues later. If defined properly, this gives more control on budget, time and quality – to both sides but the plan has to be detailed and pragmatic with all dependencies and constraints, monitoring closely with proper issue and risk management is extremely critical for the success of this approach. </p>

								<p ><strong>Wave 3: Outpace Outsourcing</strong></p>

								<p>In an age, when the need for Innovation because of Technological advancement, is more than ever and the speed of change is faster than ever, the above two waves are not sufficient. It requires a lot more. </p>

								<p>With the Technology evolution, new solutions are getting created, leading to more opportunities at the same time creating room for new unknowns. Most organizations have limited time, money and resources at their disposal to achieve a business solution. A lead organization creating an ecosystem of Partners contributing on complementary strengths could be one of the solutions. The lead organization will start becoming an orchestrator and stitch different solutions together for a substantially higher outcome. The business driver will move away from Cost Saving to expedite innovation. With Digital investments leading to higher returns in a short time, IT’s role itself is going through a change from being a Service provider to Orchestrator of solutions/ services. The IT services organizations will have an opportunity to help the client organizations for not just making IT Smarter by becoming substantially cost effective with much higher outcomes and also helping with innovation. The IT Operations, which continues to take huge share of IT expense, will go through a transformation by high degree of Automation across Applications &amp; Infrastructure. It is interesting that while IT’s job it to automate processes, but IT as a function is still quite manual. </p>

								<p>From Commercial perspective, this would mean much higher skill in the game by all partners and everyone’s fate linked to the outcome. This would mean everyone continuously contributes to the success of the whole Eco System and not just protect their own interest. This will change the way every Eco System Partner thinks and behaves.
</p>


