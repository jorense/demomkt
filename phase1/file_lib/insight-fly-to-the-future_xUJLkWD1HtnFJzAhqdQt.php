								<p><strong>Taking off with your Digital Companion</strong></p>
								<p>But what about our experience as travellers? Air travel is playing an increasingly important role in Hong Kong people’s life. Be it for holiday, business, or study, taking a flight abroad is nowadays getting common. With more and more budget airlines being available in the region, leaving town for vacation becomes much more affordable and popular. But with so many people travelling each day, while the schedules of Hong Kong people are generally quite hectic, how can our travel experience be enhanced? How can the quality of travel be improved, when the quantity of travellers is on the rise every year?</p>

								<p>Imagine a <a href="#">mobile application</a> that can consolidate all your travel arrangements, from planning to commuting to taking off, on one single platform, virtually a Digital Companion. The application would have stored all your flight bookings, so that 48 hours before a flight, a notification will be sent to your phone to remind you of the flight, weather information of the destination, packing tips, currency exchange rate, even options to purchase travel insurance. It would also help you book a seat at an airport lounge, or a taxi to the airport, should so be required.</p>

								<div class="so-full-pic-holder">
									<img class="img-auto" src="dist/images/insight/insight_p082.jpg" width="1103" height="657" alt="">
								</div>

								<p class="pt5">On the day of departure, it reminds you when it is time to head to the airport, the available modes of commute with fares and time needed. When you approach the airport, it tells you which terminal, aisle and counter to go for check-in, which would have been expedited thanks to the help of self-help luggage drop-off. Paper boarding pass will not be necessary because a digital one would have been sent to your phone. You will also receive a real-time update of your journey planner, telling you how much time it will take you to go through security and immigration, hence how much time left for leisure like shopping, relaxing in an airport lounge, etc. Of course, with the real-time function, the Digital Companion will always advise you on which the fastest route is, no matter for security check, immigration process, or accessing the lounge. If you feel like doing a bit of shopping before departure, why not? Because shopping has been made less laborious since you no longer need to carry your own purchase, they will be sent to your boarding gate for pick-up before departure. Touch wood, but in case of any flight delay, the Digital Companion will for sure notify you. So there you go, that’s how you kick off a hassle-free travel!</p>

								<p>Similarly, when you return to Hong Kong after a trip, once you have landed, the real-time Digital Companion will again update you on the time needed for going through immigration and luggage collection, the best routes to take, etc. So returning from a trip doesn’t feel that bad after all!</p>

								<p>Now, fasten your seat belt, and be ready to travel to the future!</p>


