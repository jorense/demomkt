								<div class="col-sm-12 col-md-8 in-par">
									<p>In order to ensure that all parties who needed flight information could gain access to it, the Airport Authority granted a license to PCCW in November 1997 to provide flight information to government departments, airlines, and franchisees, licensees and tenants of the authority at Chek Lap Kok as well as other users.

									<p>A high availability server was connected to the Authority's AODB for the purpose of receiving the most up-to-date flight information data and distributing it to users within and outside the airport.

