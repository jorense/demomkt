								<p><strong><span style="font-size: 25px;">T</span>he Growing Trend in IoT </strong></p>

								<p>Technology has grown far and beyond revolutionary. It has evolved in so many ways and influenced the way we conduct our daily activities and the content we consume on a daily basis. Technology has empowered us to not only connect to anyone, but also to anything, all in real-time. Part of this evolution that enables people to digitally connect and interact with the things around them is what Internet-of-Things (IoT) is all about.</p>

								<p>Based on the recent Gartner studies on IoT, it is projected that the spending on this technology will reach a combined total of US$471 billion for professional services, consumer needs, and overall connectivity services by year 2020, which is about 20% CAGR from 20151. With this demand on spending on the technology, enterprises need to provide compatible devices to support the needs of the market; thus, it is projected that there will be US$6.5 billion shipments of IoT endpoints by year 2020<sup>2</sup>. In terms of activation, it is projected that there will be US$63 million new devices activated per second, all within enterprises and consumer products by year 2020<sup>3</sup>.</p>

								<p>This trend has evolved due to increased connectivity within consumers who are always on the go and consumes huge volumes of data through their mobile devices. The growth is also affected by societal and infrastructure improvements that enable connectivity for better and faster access as they integrate and utilize IoT solutions.</p>

