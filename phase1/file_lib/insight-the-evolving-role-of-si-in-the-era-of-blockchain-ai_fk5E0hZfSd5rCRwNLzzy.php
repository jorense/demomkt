								<p><strong>New Players, New Game</strong></p>
								<p>Every industry is leveraging AI to enhance their services. Let’s take healthcare sector as an example. </p>

								<p>World Health Organization reports that the world needs 17 million more healthcare workers with developing regions like Africa and Southeast Asia alone needing 11 million4. With the rising ageing population and the tremendous lack of medical resources, there is an apparent gap. Training more people to join the healthcare workforce probably isn’t the easiest solution, leading many organizations to turn towards technology to minimize the gaps.</p>

								<p>Robots are starting to replace personnel in hospitals and nursing homes in a variety of functions, from managing patient wait times to providing anesthetics to patients. In addition, we have also seen robots serve as ‘care companions’ to the elderly, helping provide medication, and connection to smart appliances, loved ones and healthcare professionals. The accuracy of required care is improving with time using deep learning.</p>
								In a similar fashion, the healthcare industry has also been disrupted by Blockchain, with decentralized patient-provider functions which can get tedious and expensive. </p>

								<p>We’ve only spoken about one industry so far, and already the value addition of AI and blockchain are undeniable. The benefits to sectors like banking, retail, manufacturing, telecom, governments and other Industries are of a different order of magnitude when compared to traditional technologies. There are plenty of use cases getting added across industries daily, as the awareness increases and as the technologies evolve. Consumers are jumping directly to some of these new technologies solving different problems, without the baggage of any legacy platforms. </p>

								<p>The rise of new technologies has made startups the new ‘cool’, especially in this dynamic technology industry. While Ethereum, R3, Hyperledger and Ripple are some of the biggest names in the Blockchain ecosystem, the huge number of smaller players that are emerging across the globe have their own differentiation and value addition to the Blockchain eco system.</p>

								<p>The number of players in the market, continues to increase multifold, with each player capable of delivering unique solutions, addressing evolving needs of enterprises and governments. With the speed at which these new companies are coming up, finding the right solution company is becoming a new challenge. There will be many cool companies but as historical data suggests, only a few will be successful. In the last decade, we have seen that the time it takes for any company to become irrelevant has shortened significantly. </p>


								<p>Traditionally, enterprises and governments looking to transform their legacy systems turn to a system integrator to align with strategic vendors to manage their multidisciplinary needs. Now, with the increasing number of startups in the AI and Blockchain space alone, it would be difficult for enterprises to only align with the larger product vendors as a one-stop-shop. Instead, they are working with various startups to suit their changing business needs. This is particularly true for addressing New Age technologies and solving niche problems. The time to show results with any transformation has also gone down significantly. Typically, technology led business transformation takes a few years to implement, with huge amount of investment. Now, there is hesitation to go on such a journey, with the preference being to get results quickly - in weeks and months – as the macro environment around an organization is changing swiftly. If solution implementation takes too long, by the time the solution is implemented, the market has already moved forward making the solution less relevant. Hence, the increasing motivation is to look for an effective niche solutions that can solve specific problems swiftly, cost effective, and easy to customize.</p>


