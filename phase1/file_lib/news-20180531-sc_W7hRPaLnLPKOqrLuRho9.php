								<p>电讯盈科（股份代号：0008）【北京．2018年5月31日】电讯盈科有限公司旗下的信息技术服务旗舰电讯盈科企业方案今天推出Infinitum™ Visum应用方案。Infinitum™ Visum是一个连接互联网的智能镜子，结合互动的高清触控式屏幕及预装一系列提高生产力、生活便利及娱乐的应用程序。Infinitum™ Visum推动数字化互联的生活方式，有助人们生活、工作及娱乐方式的转型。 </p>

								<p>Infinitum™ Visum将会推出针对不同行业的服务组合，并会按行业预装相关的应用程序，适用于包括房地产开发商、酒店及零售店。Visum的多功能系统让房地产开发商将家居自动化的创意带给住客，为酒店业革新客户体验，以及让零售商为顾客提供店内虚拟试穿及无缝的线上至线下购物体验。</p>
								 
								<p>Infinitum™ Visum透过内置的Wi-Fi及蓝牙连接技术，支持连接健康追踪器、电子秤及血压计等物联网智能装置，以及一系列的医疗设施，将外部信息带入容易接达及现代时尚的家电中。Infinitum™ Visum亦支持OTT视象服务及音乐串流等娱乐应用程式，丰富终端用户的日常生活。电讯盈科企业方案为企业度身定制解决方案，包括硬件、软件配置及用户体验优化，并提供后端数据分析、报告及其他服务，以满足客户的需求。</p>


								<p>电讯盈科企业方案董事总经理于礼安先生表示：「结合电讯盈科集团的互联、娱乐及信息技术能力，Infinitum™ Visum是一个独特及智能的方案，可配置在不断扩大的数字化世界中提升数字化互联生活。电讯盈科企业方案致力带来创新的应用方案，包括Visum及Infinitum™旗下其他的应用方案，以支持不同行业的企业进行数字化转型。」</p> 

								<p>Visum为英文View的拉丁文，意指「视野」，而Infinitum™为电讯盈科企业方案的多元化应用方案，包括云、人工智能、数字化、进阶分析及物联网五大范畴。有关Infinitum™ Visum的详情及服务组合，请浏览<a href="www.pccwsolutions.com/visum" style="color: #337ab7;" target="_blank">www.pccwsolutions.com/visum</a>。 </p>


